    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        int index = tblDichVu.getSelectedRow();
        if (index == -1) {
        JOptionPane.showMessageDialog(this, "Vui lòng chọn dịch vụ để sửa");
        return;
        }
    
        hienThiDialog(true);
        DichVuDialog.setLocationRelativeTo(null);
        DichVuDialog.setVisible(true);
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
            DichVu dv = this.getFormData();
            DVRepo.update(dv);
            JOptionPane.showMessageDialog(this, "Sửa Thành Công");
            loadToTable(DVRepo.search(""));
            DichVuDialog.dispose();
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
            DichVu dv = this.getFormData();
            DVRepo.delete(dv.getMaDV());
            JOptionPane.showMessageDialog(this, "Xóa Thành Công");
            loadToTable(DVRepo.search(""));
            hienThi(0);
    }//GEN-LAST:event_btnDeleteActionPerformed


