    public DichVu getFormData(){
        String maDV = txtMaDV.getText();
        String tenDV = txtTenDV.getText();
        String maLDV = txtMaLDV.getText();
        float gia = Float.parseFloat(txtGia.getText());
        
        return new DichVu(maDV, tenDV, maLDV, gia);
    }
    
     public void loadToTable(ArrayList<DichVu> ds){
         DefaultTableModel tblModel =(DefaultTableModel) tblDichVu.getModel();
        tblModel.setRowCount(0);
        for(DichVu dv:ds){
            Object[] data ={dv.getMaLDV(),dv.getMaDV(),dv.getTenDV(),dv.getGia()};
            tblModel.addRow(data);
        }
    }
     
      public void hienThi(int index){
        txtMaLDV.setText(tblDichVu.getValueAt(index, 0).toString());
        txtMaDV.setText(tblDichVu.getValueAt(index, 1).toString());
        txtTenDV.setText(tblDichVu.getValueAt(index, 2).toString());
        txtGia.setText(tblDichVu.getValueAt(index, 3).toString());
    }
      
         public boolean check(String ma){
        for(int i=0;i<ds.size();i++){
            if(ds.get(i).getMaDV().equalsIgnoreCase(ma)){
                return true;
            }
        }
        return false;
    }
         
         public void newTable(){
             txtMaLDV.setText("");
             txtMaDV.setText("");
             txtTenDV.setText("");
             txtGia.setText("");
         }

         
   public void hienThiDialog(boolean isSua){
    if(isSua) {
        btnAdd.setVisible(false); // Ẩn nút Thêm
        btnUpdate.setVisible(true); // Hiện nút Sửa
        txtMaDV.setEditable(false);
        txtMaLDV.setEditable(false);
    } else {
        btnAdd.setVisible(true);  // Hiện nút Thêm
        btnUpdate.setVisible(false);  // Ẩn nút Sửa
        txtMaDV.setEditable(true);
        txtMaLDV.setEditable(true);
    }
}
      
   public class TableUtils {

    public static void setHeaderStyle(JTable table) {
        JTableHeader tableHeader = table.getTableHeader();
        tableHeader.setFont(new Font("Roboto", Font.PLAIN, 14));
        DefaultTableCellRenderer headerRenderer = new DefaultTableCellRenderer();
        headerRenderer.setBackground(Color.decode("#89cff0"));   
        headerRenderer.setForeground(Color.WHITE);  

        // Áp dụng renderer cho tất cả các cột
        for (int i = 0; i < tableHeader.getColumnModel().getColumnCount(); i++) {
            tableHeader.getColumnModel().getColumn(i).setHeaderRenderer(headerRenderer);
        }
    }
}
    
